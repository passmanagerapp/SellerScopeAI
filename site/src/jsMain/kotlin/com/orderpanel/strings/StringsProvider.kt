private val xmlStringRegex = Regex("""<string name="([^"]+)">([\s\S]*?)</string>""")

private fun parseXml(xml: String): Map<String, String> =
    xmlStringRegex.findAll(xml).associate { it.groupValues[1] to it.groupValues[2].unescapeXml() }

private fun String.unescapeXml() = this
    .replace("&amp;", "&")
    .replace("&lt;", "<")
    .replace("&gt;", ">")
    .replace("&quot;", "\"")
    .replace("&apos;", "'")

